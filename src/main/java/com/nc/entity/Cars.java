package com.nc.entity;

public class Cars {
	private String carName;
	private double price;
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public Cars() {
		super();
	}
}
